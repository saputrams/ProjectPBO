public class RekapCalon {
    String namaCalon;
    String namaLembaga;
    String tipeCalon;
    int jumlahSuara;

    public String getNamaCalon() {
        return namaCalon;
    }

    public void setNamaCalon(String namaCalon) {
        this.namaCalon = namaCalon;
    }

    public String getNamaLembaga() {
        return namaLembaga;
    }

    public void setNamaLembaga(String namaLembaga) {
        this.namaLembaga = namaLembaga;
    }

    public String getTipeCalon() {
        return tipeCalon;
    }

    public void setTipeCalon(String tipeCalon) {
        this.tipeCalon = tipeCalon;
    }

    public int getJumlahSuara() {
        return jumlahSuara;
    }

    public void setJumlahSuara(int jumlahSuara) {
        this.jumlahSuara = jumlahSuara;
    }
    
    
}
